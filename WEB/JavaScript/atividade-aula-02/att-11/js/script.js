var valor = parseInt(prompt("Escolha entre 1 ate 4:"));

switch(valor){
    case 1:
        document.write("1");
        break
    case 2:
        document.write("2");
        break
    case 3:
        document.write("3");
        break
    case 4:
        document.write("4");
        break
    default:
        document.write("ultimo");
}